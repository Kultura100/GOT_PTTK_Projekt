                    <div class="col col-t">1. Zadaniem przodownika turystyki górskiej jest praca nad upowszechnianiem i podnoszeniem poziomu uprawiania turystyki górskiej, w szczególności:</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">a. inicjowanie i organizowanie działalności w zakresie turystyki górskiej oraz uczestniczenie w życiu organizacyjnym PTTK. Oznacza to, że każdy przodownik winien być aktywnym działaczem odpowiedniego szczebla podstawowego działalności PTTK (koła, klubu, oddziału)</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">b. organizowanie i prowadzenie wycieczek i imprez turystyki górskiej (w tym obozów PTTK), prowadzenie szkolenia, współdziałanie w organizowaniu kursów szkoleniowych oraz pełnienie funkcji na terenowych i centralnych imprezach PTTK</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">c. nadsyłanie do właściwych ogniw PTTK uwag i spostrzeżeń o zagospodarowaniu turystycznym, stanie szlaków oraz obsłudze ruchu turystycznego, a także podejmowanie doraźnych interwencji na miejscu w przypadku rażących braków i niedopatrzeń.</div>
                </li>

                <li class="table-row">
                    <div class="col col-t">2. Rola przodownika nie może ograniczać się do czynności wyłączne organizacyjnych i technicznych, lecz musi być poszerzona o rolę animatora i popularyzatora turystyki górskiej oraz szeroko rozumianej kultury górskiej w różnych środowiskach społecznych: zakładzie pracy, osiedli, szkole, stowarzyszeniach i organizacjach. Powinien również stymulować procesy uczestnictwa w kulturze gór, rozbudzać i utrwalać zainteresowania u ludzi, których wprowadza w świat gór.</div>
                </li>

                <li class="table-row">
                    <div class="col col-t">3. Praca w terenie jest najważniejszym przejawem działalności przodownika. Przodownik winien dbać o to, aby wycieczki przez niego prowadzone były wzorowe, a ich uczestnicy wynosili z nich zadowolenie i wszechstronne korzyści.</div>
                </li>

                <li class="table-row">
                    <div class="col col-t">4. Organizując wycieczkę przodownik powinien:</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">a. dokładnie opracować trasę i program</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">b. opracować dokładny czasowy przebieg wycieczki, przewidując rezerwę czasu na opóźnienia z różnych przyczyn</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">c. przygotować uczestników, podając im dokładne informacje o programie i trasie wycieczki oraz wskazówki co do ubioru, sprzętu i wyposażenia, noclegów i warunków wyżywienia, przejazdów, kosztów, itp.</div>
                </li>

                <li class="table-row">
                    <div class="col col-t">5. Prowadząc wycieczkę przodownik winien pamiętać, że ponosi odpowiedzialność za prowadzoną przez niego grupę. W szczególności przodownik winien </div>
                </li>
                <li class="table-row">
                    <div class="col col-1">a. dbać, by uczestnicy wycieczki byli należycie ubrani, obuci i wyposażeni w odpowiedni sprzęt turystyczny</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">b. dbać o dobrą kondycję fizyczną i psychiczną uczestników wycieczki, w szczególności o to, aby tempo marszu i czas odpoczynków dostosowane były do możliwości słabszych uczestników</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">c. dbać, by prowadzona grupa przez cały czas wycieczki stanowiła zwarty zespół. Zwartość zespołu jest podstawowym warunkiem bezpieczeństwa w górach. Przodownik nie może pod żadnym pozorem odłączyć się od grupy. Przodownik musi dbać, by nikt z uczestników wycieczki nie pozostał w terenie sam bez opieki - zdrowy czy tym bardziej chory</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">d. w ramach większych zespołów mianować swego zastępcę lub zastępców, opiekujących się mniejszymi grupami i czuwającymi nad słabszymi uczestnikami wycieczki. Zastępca winien iść na końcu grupy</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">e. podawać dokładne informacje i wskazówki mające na celu przekazanie uczestnikom wycieczki istotnych wiadomości krajoznawczych o zwiedzanym terenie, jak również z zakresu prawidłowego uprawiania turystyki górskiej. Przodownik winien wykorzystać wycieczkę do udzielenia praktycznych wskazówek dotyczących ochrony przyrody, korzystanie z urządzeń turystycznych, orientacji w terenie, itp.</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">f. do uczestników wycieczki odnosić się z życzliwością i okazywać im opiekę pod każdym względem; pod żadnym pozorem nie dopuszczać do spożywania alkoholu oraz interweniować w przypadku niewłaściwego zachowania uczestników wycieczki</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">g. posiadać należycie zaopatrzoną apteczkę pierwszej pomocy oraz udzielać pierwszej pomocy w razie potrzeby</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">h. zwracać uwagę uczestnikom wycieczki na niebezpieczeństwa gór, uczyć ich bezpiecznego uprawiania turystyki górskiej.</div>
                </li>

                <li class="table-row">
                    <div class="col col-t">6. Przodownik jako członek Polskiego Towarzystwa Turystyczno-Krajoznawczego powinien:</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">a. godnie reprezentować towarzystwo</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">b. zwracać uwagę na stan zagospodarowania turystycznego (szlaku, schroniska, pola biwakowe, itp.), interweniować w przypadku naruszenia ogólnie przyjętych zasad uprawiania turystyki oraz zawiadamiać odpowiednie jednostki PTTK o zauważonych brakach i usterkach</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">c. zwracać uwagę na przestrzeganie zasad ochrony przyrody i interweniować w przypadku ich naruszania</div>
                </li>
                <li class="table-row">
                    <div class="col col-1">d. nosić odznakę przodownicką podczas pobytu w górach.</div>
                </li>

